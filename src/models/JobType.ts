import mongoose from 'mongoose';

const jobTypeSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
    },
  },
  {
    timestamps: true,
  }
);

const JobType = mongoose.model('JobType', jobTypeSchema);

export default JobType;
